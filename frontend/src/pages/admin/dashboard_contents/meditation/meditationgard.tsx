import React, { useEffect, useState } from "react";
import {
  ResponsiveContainer,
  PieChart,
  Pie,
  Cell,
  Tooltip,
  Legend,
} from "recharts";
import { EllipsisOutlined } from "@ant-design/icons"; 
import { getSoundFourType, DailySoundUsage } from "../../../../services/https/dashboardcontents";
import MonthPickerMed from "./monthpicker";

interface MusicData {
  category: string; // ‡∏™‡∏°‡∏≤‡∏ò‡∏¥, ‡∏™‡∏ß‡∏î‡∏°‡∏ô‡∏ï‡πå, ‡∏ù‡∏∂‡∏Å‡∏´‡∏≤‡∏¢‡πÉ‡∏à, ASMR
  plays: number;
}

interface MusicCardProps {
  title?: string;
  className?: string;
  onViewMore?: () => void;
}

// üé® ‡∏™‡∏µ‡∏û‡∏≤‡∏™‡πÄ‡∏ó‡∏•‡∏ï‡∏≤‡∏°‡∏ó‡∏µ‡πà‡∏Ñ‡∏∏‡∏ì‡∏Å‡∏≥‡∏´‡∏ô‡∏î
const CATEGORY_COLORS: Record<string, string> = {
  "‡∏™‡∏°‡∏≤‡∏ò‡∏¥": "#7bed7f",    // ‡πÄ‡∏Ç‡∏µ‡∏¢‡∏ß‡∏°‡∏¥‡πâ‡∏ô‡∏ï‡πå
  "‡∏™‡∏ß‡∏î‡∏°‡∏ô‡∏ï‡πå": "#AFD5F0",  // ‡∏ü‡πâ‡∏≤‡∏≠‡πà‡∏≠‡∏ô
  "‡∏ù‡∏∂‡∏Å‡∏´‡∏≤‡∏¢‡πÉ‡∏à": "#ffbc59", // ‡∏™‡πâ‡∏°‡∏û‡∏≤‡∏™‡πÄ‡∏ó‡∏•
  "ASMR": "#ee8fff",      // ‡∏°‡πà‡∏ß‡∏á‡∏ä‡∏°‡∏û‡∏π
};

const MusicCard: React.FC<MusicCardProps> = ({
  title = "‡∏Ñ‡∏≠‡∏ô‡πÄ‡∏ó‡∏ô‡∏ï‡πå‡πÄ‡∏™‡∏µ‡∏¢‡∏á",
  className = "bg-white",
  onViewMore,
}) => {
  const [data, setData] = useState<MusicData[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [selectedMonth, setSelectedMonth] = useState<Date>(new Date());

  useEffect(() => {
    const fetchMusicData = async () => {
      setLoading(true);
      try {
        const response: DailySoundUsage[] = await getSoundFourType();
        console.log("Raw data from API (four-type):", response);

        // ‡∏Å‡∏£‡∏≠‡∏á‡∏ï‡∏≤‡∏°‡πÄ‡∏î‡∏∑‡∏≠‡∏ô-‡∏õ‡∏µ‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å
        const filtered = response.filter(
          (item) =>
            item.year === selectedMonth.getFullYear() &&
            item.month === selectedMonth.getMonth() + 1
        );

        // ‡∏£‡∏ß‡∏°‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡πÄ‡∏•‡πà‡∏ô‡∏ï‡∏≤‡∏°‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó
        const formattedData = filtered.reduce((acc: MusicData[], item) => {
          const categoryName = item.category === "asmr" ? "ASMR" : item.category; // normalize
          const existing = acc.find((d) => d.category === categoryName);
          if (existing) {
            existing.plays += item.play_count;
          } else {
            acc.push({ category: categoryName, plays: item.play_count });
          }
          return acc;
        }, []);

        setData(formattedData);
      } catch (err) {
        setError("‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ");
        console.error(err);
      } finally {
        setLoading(false);
      }
    };

    fetchMusicData();
  }, [selectedMonth]);

  const totalPlays = data.reduce((sum, item) => sum + item.plays, 0);

  return (
    <div className={`rounded-2xl p-4 shadow-md ${className} relative`}>
      <h2 className="font-semibold mb-2 flex items-center justify-between">
        {title}
        {onViewMore && (
          <button
            onClick={onViewMore}
            className="p-2 rounded-full bg-white/50 hover:bg-blue-300 transition flex justify-center items-center"
            title="‡∏î‡∏π‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏û‡∏¥‡πà‡∏°‡πÄ‡∏ï‡∏¥‡∏°"
          >
            <EllipsisOutlined className="text-white text-lg" />
          </button>
        )}
      </h2>

      {/* Month Picker */}
      <div className="mb-3">
        <MonthPickerMed value={selectedMonth} onChange={setSelectedMonth} />
      </div>

      {loading ? (
        <p>‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•...</p>
      ) : error ? (
        <p className="text-red-500">{error}</p>
      ) : data.length === 0 ? (
        <p>‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÄ‡∏î‡∏∑‡∏≠‡∏ô‡∏ô‡∏µ‡πâ</p>
      ) : (
        <>
          <div className="h-64">
            <ResponsiveContainer width="100%" height="100%">
              <PieChart>
                <Pie
                  data={data}
                  dataKey="plays"
                  nameKey="category"
                  cx="50%"
                  cy="50%"
                  outerRadius={100}
                  label={false}
                  labelLine={false}
                >
                  {data.map((entry, index) => (
                    <Cell
                      key={`cell-${index}`}
                      fill={CATEGORY_COLORS[entry.category] || "#cccccc"} // ‡πÉ‡∏ä‡πâ‡∏™‡∏µ‡∏à‡∏≤‡∏Å category
                    />
                  ))}
                </Pie>
                <Tooltip formatter={(value: number) => [`${value} ‡∏Ñ‡∏£‡∏±‡πâ‡∏á`, "‡πÄ‡∏•‡πà‡∏ô"]} />
                <Legend />
              </PieChart>
            </ResponsiveContainer>
          </div>
          <p className="text-lg font-bold mt-2 font-ibmthai">{totalPlays} ‡∏Ñ‡∏£‡∏±‡πâ‡∏á</p>
        </>
      )}
    </div>
  );
};

export default MusicCard;
